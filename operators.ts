let add = (num1 : number , num2 : number) => {

    return num1+num2;
}
let subtract = (num1 : number , num2 : number) => {

    return num1 - num2; 
}
let multiply = (num1 : number , num2 : number) => {

    return num1 * num2; 
}
let division = (num1 : number , num2 : number) => {

    return num1 / num2; 
}

export {add , subtract , multiply , division};